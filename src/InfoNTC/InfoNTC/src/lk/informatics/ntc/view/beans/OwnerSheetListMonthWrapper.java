package lk.informatics.ntc.view.beans;

public class OwnerSheetListMonthWrapper {
	private final String[] list;
    private final String month;

    public OwnerSheetListMonthWrapper(String[] list, String month) {
        this.list = list;
        this.month = month;
    }

    public String[] getList() {
        return list;
    }

    public String getMonth() {
        return month;
    }
}
