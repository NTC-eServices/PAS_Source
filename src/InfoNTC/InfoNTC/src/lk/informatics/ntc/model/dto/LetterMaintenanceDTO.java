package lk.informatics.ntc.model.dto;

public class LetterMaintenanceDTO extends ProgramDTO{
	
	private static final long serialVersionUID = 5263526329645L;
	
	private Long pld_seq;
	private String year;
	private String zoneIssueDate;
	private String schoolName;
	private String schoolLetterDate;
	private String policeName;
	private String policeLetterDate;
	
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getZoneIssueDate() {
		return zoneIssueDate;
	}
	public void setZoneIssueDate(String zoneIssueDate) {
		this.zoneIssueDate = zoneIssueDate;
	}
	public String getSchoolName() {
		return schoolName;
	}
	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}
	public String getSchoolLetterDate() {
		return schoolLetterDate;
	}
	public void setSchoolLetterDate(String schoolLetterDate) {
		this.schoolLetterDate = schoolLetterDate;
	}
	public String getPoliceName() {
		return policeName;
	}
	public void setPoliceName(String policeName) {
		this.policeName = policeName;
	}
	public String getPoliceLetterDate() {
		return policeLetterDate;
	}
	public void setPoliceLetterDate(String policeLetterDate) {
		this.policeLetterDate = policeLetterDate;
	}
	public Long getPld_seq() {
		return pld_seq;
	}
	public void setPld_seq(Long pld_seq) {
		this.pld_seq = pld_seq;
	}
	
}
