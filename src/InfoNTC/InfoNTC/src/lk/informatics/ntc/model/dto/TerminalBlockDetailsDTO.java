package lk.informatics.ntc.model.dto;

import java.io.Serializable;
import java.sql.Timestamp;

public class TerminalBlockDetailsDTO implements Serializable {

	private static final long serialVersionUID = 1L;
	
	private String stationCode;
	private int noOfBlocks;
	private String blockDisplayType= "N"; 	
	private String blockIdStartWith;
	private String status;
	
	private long seq;
	private String terminal;
	private String platform;
	private String block;
	private String terminalDetailsId;

	private String createdBy;
	private Timestamp createdDate;
	private String modifiedBy;
	private Timestamp modifiedDate;
	
	private long terminalBlockSeq;
	
	public String getStationCode() {
		return stationCode;
	}
	public void setStationCode(String stationCode) {
		this.stationCode = stationCode;
	}
	public int getNoOfBlocks() {
		return noOfBlocks;
	}
	public void setNoOfBlocks(int noOfBlocks) {
		this.noOfBlocks = noOfBlocks;
	}
	public String getBlockDisplayType() {
		return blockDisplayType;
	}
	public void setBlockDisplayType(String blockDisplayType) {
		this.blockDisplayType = blockDisplayType;
	}
	public String getBlockIdStartWith() {
		return blockIdStartWith;
	}
	public void setBlockIdStartWith(String blockIdStartWith) {
		this.blockIdStartWith = blockIdStartWith;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public long getSeq() {
		return seq;
	}
	public void setSeq(long seq) {
		this.seq = seq;
	}
	public String getTerminal() {
		return terminal;
	}
	public void setTerminal(String terminal) {
		this.terminal = terminal;
	}
	public String getPlatform() {
		return platform;
	}
	public void setPlatform(String platform) {
		this.platform = platform;
	}
	public String getCreatedBy() {
		return createdBy;
	}
	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}
	public Timestamp getCreatedDate() {
		return createdDate;
	}
	public void setCreatedDate(Timestamp createdDate) {
		this.createdDate = createdDate;
	}
	public String getModifiedBy() {
		return modifiedBy;
	}
	public void setModifiedBy(String modifiedBy) {
		this.modifiedBy = modifiedBy;
	}
	public Timestamp getModifiedDate() {
		return modifiedDate;
	}
	public void setModifiedDate(Timestamp modifiedDate) {
		this.modifiedDate = modifiedDate;
	}
	public String getTerminalDetailsId() {
		return terminalDetailsId;
	}
	public void setTerminalDetailsId(String terminalDetailsId) {
		this.terminalDetailsId = terminalDetailsId;
	}
	public String getBlock() {
		return block;
	}
	public void setBlock(String block) {
		this.block = block;
	}
	public long getTerminalBlockSeq() {
		return terminalBlockSeq;
	}
	public void setTerminalBlockSeq(long terminalBlockSeq) {
		this.terminalBlockSeq = terminalBlockSeq;
	}
	
	
}
